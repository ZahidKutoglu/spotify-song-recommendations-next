import { getSession } from 'next-auth/react';

export default async function handler(req, res) {
  const session = await getSession({ req });

  if (!session) {
    return res.status(401).json({ error: 'Not authenticated' });
  }

  console.log('Session:', session);  // Check the session data

  const { name } = req.body;

  if (!name) {
    return res.status(400).json({ error: 'Missing required field: name' });
  }

  if (!session.accessToken) {
    return res.status(401).json({ error: 'Access token not available' });
  }

  try {
    // Pass the session's accessToken in the Authorization header
    const playlistRes = await fetch('https://api.spotify.com/v1/users/' + session.user.id + '/playlists', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${session.accessToken}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        name: name,
        description: 'New playlist generated by the app',
        public: false,
      }),
    });

    const playlist = await playlistRes.json();

    if (playlistRes.status !== 201) {
      return res.status(playlistRes.status).json({ error: playlist.error });
    }

    res.status(200).json(playlist);
  } catch (error) {
    console.error('Error creating playlist:', error);
    res.status(500).json({ error: 'Internal server error' });
  }
}
